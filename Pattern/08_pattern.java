package Pattern;

 class pattern08 {
    public static void main(String[] args) {
        int n=4;
        for(int row=1;row<=n;row++){
            int i=1;
            for(int col=1;col<=n;col++){
                System.out.print(i + " ");
                i++;
            }
            System.out.println();
        }
    }
    
}
