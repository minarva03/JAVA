package Numbers;

public class floydsTriangle {
    public static void main(String[] args) {
        int n=5,n2=1;;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print(n2+" " );
                n2++;
                
            }
            System.out.println("");
        }
    }
    
}
